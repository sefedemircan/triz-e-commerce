// Her i≈ülem i√ßin yapƒ±lacak aksiyonlar
const ACTIONS = {
  order_status: async (userId) => {
    const { data: orders } = await supabase
      .from('orders')
      .select('*')
      .eq('user_id', userId)
      .order('created_at', { ascending: false })
      .limit(1);
    
    if (!orders?.length) {
      return 'Aktif sipari≈üiniz bulunmamaktadƒ±r.';
    }

    const order = orders[0];
    const statusMap = {
      pending: 'Onay Bekliyor',
      processing: 'Hazƒ±rlanƒ±yor',
      shipped: 'Kargoya Verildi',
      delivered: 'Teslim Edildi',
      cancelled: 'ƒ∞ptal Edildi'
    };

    return `Son sipari≈üinizin durumu: ${statusMap[order.status]}\nSipari≈ü Tarihi: ${new Date(order.created_at).toLocaleDateString('tr-TR')}`;
  },
  
  last_orders: async (userId) => {
    const { data: orders } = await supabase
      .from('orders')
      .select(`
        *,
        order_items (
          quantity,
          products (
            name,
            price
          )
        )
      `)
      .eq('user_id', userId)
      .order('created_at', { ascending: false })
      .limit(5);

    if (!orders?.length) {
      return 'Hen√ºz bir sipari≈ü ge√ßmi≈üiniz bulunmamaktadƒ±r.';
    }

    let response = 'Son 5 sipari≈üiniz:\n\n';
    orders.forEach(order => {
      response += `üì¶ Sipari≈ü #${order.id}\n`;
      response += `Tarih: ${new Date(order.created_at).toLocaleDateString('tr-TR')}\n`;
      response += `Tutar: ${order.final_amount.toLocaleString('tr-TR')} TL\n`;
      response += `√úr√ºnler:\n`;
      order.order_items.forEach(item => {
        response += `- ${item.products.name} (${item.quantity} adet)\n`;
      });
      response += '\n';
    });

    return response;
  },

  profile_info: async (userId) => {
    const { data: profile } = await supabase
      .from('profiles')
      .select('*')
      .eq('id', userId)
      .single();

    if (!profile) {
      return 'Profil bilgilerinize ula≈üƒ±lamadƒ±.';
    }

    return `Profil Bilgileriniz:\n\nAd: ${profile.full_name || 'Belirtilmemi≈ü'}\nE-posta: ${profile.email}\nTelefon: ${profile.phone || 'Belirtilmemi≈ü'}\nDoƒüum Tarihi: ${profile.birth_date ? new Date(profile.birth_date).toLocaleDateString('tr-TR') : 'Belirtilmemi≈ü'}`;
  },

  // Diƒüer aksiyonlar i√ßin benzer ≈üekilde fonksiyonlar eklenebilir...
};

